export function getLocaleName(englishName: string, locale: string): string {
  if (locale === "en") return englishName;
  const areaMatch = areaNames[englishName];
  if (areaMatch?.[locale]) return areaMatch[locale];
  const commMatch = communityNames[englishName];
  if (commMatch?.[locale]) return commMatch[locale];
  return englishName;
}

export function getLocaleNameWithEnglish(englishName: string, locale: string): string {
  if (locale === "en") return englishName;
  const local = getLocaleName(englishName, locale);
  if (local === englishName) return englishName;
  return `${local} (${englishName})`;
}
